class Solution {
  /**
   * @param {number[]} prices
   * @return {number}
   */
  maxProfit(prices) {
    let profit = 0;
    let minPrice = prices[0];

    if (1 <= prices.length <= 100) {
      for (let i = 0; i < prices.length; i++) {
        if (0 <= prices[i] <= 100) {
          minPrice = Math.min(minPrice, prices[i]);
          profit = Math.max(profit, prices[i] - minPrice);
        }
      }
      return profit;
    }
  }
}

